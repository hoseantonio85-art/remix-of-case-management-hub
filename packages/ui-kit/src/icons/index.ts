type SvgIconProperties = React.SVGProps<SVGSVGElement> & {
  children?: React.ReactNode;
};

type SvgIconComponent = (properties: SvgIconProperties) => React.ReactElement;

const createSpriteIcon = (symbolId: string): SvgIconComponent => {
  const SpriteIcon: SvgIconComponent = ({
    children,
    height = 24,
    width = 24,
    ...properties
  }) =>
    React.createElement(
      'svg',
      { ...properties, height, width },
      children,
      React.createElement('use', { href: '#' + symbolId }),
    );

  return SpriteIcon;
};

